def test_location_name_formats():
    """Names should be: adj+noun, noun only, or noun+number."""
    one_word = False
    two_word_alpha = False
    with_number = False
    for seed in range(50):
        g = Galaxy(seed=seed)
        _explore_n(g, 10)
        for system in g.systems.values():
            for loc in system.locations:
                parts = loc.name.split()
                if len(parts) == 1:
                    one_word = True
                elif len(parts) == 2 and parts[-1].isdigit():
                    with_number = True
                elif len(parts) == 2 and parts[-1].isalpha():
                    two_word_alpha = True
    assert one_word, "Expected some single-noun names"
    assert two_word_alpha, "Expected some adjective+noun names"
    assert with_number, "Expected some noun+number names"


def test_arrive_expands_frontier():
    """Arriving at a system should generate new connected systems."""
    g = Galaxy(seed=1)
    home = g.systems[g.home_system]
    neighbor_name = next(iter(home.connections))
    systems_before = len(g.systems)
    g.arrive_at(neighbor_name)
    # The neighbor should now have its frontier expanded (new systems generated)
    assert len(g.systems) >= systems_before


def test_arrive_idempotent():
    """Arriving at the same system twice should not change anything."""
    g = Galaxy(seed=1)
    home = g.systems[g.home_system]
    neighbor_name = next(iter(home.connections))
    g.arrive_at(neighbor_name)
    systems_after_first = dict(g.systems)
    connections_after_first = {n: dict(s.connections) for n, s in g.systems.items()}
    g.arrive_at(neighbor_name)
    assert set(g.systems.keys()) == set(systems_after_first.keys())
    for name in g.systems:
        assert g.systems[name].connections == connections_after_first[name]


def test_travel_cost_frontier_system():
    """travel_cost() returns 2 for a frontier (unexplored) system."""
    g = Galaxy(seed=1)
    home = g.systems[g.home_system]
    neighbor_name = next(iter(home.connections))
    # Neighbor was generated by home expansion but never arrived at -> frontier
    assert neighbor_name in g._unexplored_frontier
    assert g.travel_cost(neighbor_name) == 2


def test_travel_cost_explored_system():
    """travel_cost() returns 1 for an explored (visited) system."""
    g = Galaxy(seed=1)
    home = g.systems[g.home_system]
    neighbor_name = next(iter(home.connections))
    g.arrive_at(neighbor_name)
    # After arriving, it's no longer frontier
    assert neighbor_name not in g._unexplored_frontier
    assert g.travel_cost(neighbor_name) == 1


def test_unique_location_name_fallback():
    """Fallback path must not raise when all simple attempts collide."""
    import random as _random

    from world.galaxy import _unique_location_name

    rng = _random.Random(0)
    type_words = {"adjectives": ["Old"], "nouns": ["Hulk"]}
    used: set[str] = set()
    # Fill up the obvious names so the fallback fires
    used.add("Old Hulk")
    used.add("Hulk")
    for i in range(2, 1000):
        used.add(f"Hulk {i}")
    name = _unique_location_name(rng, type_words, used, max_attempts=0)
    assert name not in used - {name}, "returned name should be unique"


def test_dreadnought_name_reserved():
    """The Dreadnought system name must never be generated for a regular system."""
    from world.galaxy import DREADNOUGHT_SYSTEM_NAME

    for seed in range(100):
        g = Galaxy(seed=seed)
        _explore_n(g, 20)
        for name, sys in g.systems.items():
            if name == DREADNOUGHT_SYSTEM_NAME:
                # Only valid if it was explicitly spawned
                assert g.dreadnought_system == name
