"""Tests for galaxy, star system, and location generation."""

from data.names import LOCATION_TYPES, LOCATION_WORDS, SYSTEM_WORDS
from data.star_types import STAR_TYPES
from world.galaxy import Galaxy


def _explore_n(g, max_systems=15):
    """BFS-explore up to max_systems systems."""
    explored = set()
    queue = [g.home_system]
    while queue and len(explored) < max_systems:
        name = queue.pop(0)
        if name in explored:
            continue
        explored.add(name)
        g.arrive_at(name)
        for neighbor in g.systems[name].connections:
            if neighbor not in explored:
                queue.append(neighbor)


